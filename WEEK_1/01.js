// 변수의 5가지 주요 개념


// 변수 이름 : 저장된 값의 고유 이름
// 변수 값 : 변수에 저장된 값
// 변수 할당 : 변수에 값을 저장하는 행위
// 변수 선언 : 변수를 사용하기 위해 컴퓨터에 알리는 행위
// 변수 참조 : 변수에 할당된 값을 읽어오는 것 

// 변수를 선언하는 3가지 방법 : var , let , const
// 1. var
// 재선언, 재할당 가능
var myVar = "Hello World!"

var myVar1;
myVar1 = 3;

// 2 . let
// 재할당 가능
let myLet = "Hello World!"
console.log(let)

// 3. const
const myConst = "Hello World!"
console.log(myConst)